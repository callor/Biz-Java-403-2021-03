package com.callor.hello;

/*
 * ============================
 * 33 + 2 =
 * 33 - 2 =
 * 33 * 2 =
 * 33 / 2 =
 * 33 % 2 =
 * ============================
 */
public class Numbers01 {
	public static void main(String[] args) {
		
		System.out.println("=====================");
		System.out.println("33 + 2 = " + (33 + 2));
		System.out.println("33 - 2 = " + (33 - 2));
		System.out.println("33 x 2 = " + (33 * 2));
		System.out.println("33 / 2 = " + (33 / 2));
		System.out.println("33 % 2 = " + (33 % 2));
		System.out.println("=======================");
		
		System.out.println("=======================");
		System.out.print("33 + 2 = ");
		System.out.println(33 + 2);
		System.out.print("33 - 2 = ");
		System.out.println(33 - 2);
		
		
		
		
		
		
		
	}
}
